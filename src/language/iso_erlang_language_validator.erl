%% @doc Checks if the given bitstring is a valid two letter language code (ISO alpha-2) OR
%%      a valid three letter language code (ISO alpha-3). This is case-sensitive (Expects uppercase).
%% @end
is_language_lower(Language) when bit_size(Language) =:= 16 -> is_language_alpha_2_lower(Language);
is_language_lower(Language) when bit_size(Language) =:= 24 -> is_language_alpha_3_lower(Language);
is_language_lower(_Other) -> false.

